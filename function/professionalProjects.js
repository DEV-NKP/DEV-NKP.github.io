    // Function to render case studies
    function renderCaseStudies(filter = 'all') {
      const container = document.getElementById('case-studies-grid');
      
      // Convert caseStudies object to array
      let caseStudiesArray = Object.entries(caseStudies)
        .map(([id, project]) => ({ id, ...project }));
      
      // Filter projects based on selected filter
      if (filter !== 'all') {
        caseStudiesArray = caseStudiesArray.filter(project => 
          project.category.toLowerCase() === filter.toLowerCase()
        );
      }
      
      // Sort by title
      caseStudiesArray.sort((a, b) => a.title.localeCompare(b.title));
      
      container.innerHTML = caseStudiesArray.map((project, index) => `
        <article class="case-study-card" itemscope itemtype="https://schema.org/Project" style="animation-delay: ${index * 0.1}s">
          <meta itemprop="position" content="${index + 1}">
          <meta itemprop="keywords" content="${project.keywords.join(', ')}">
          <div class="card-tilt">
            <div class="card-tilt-inner">
              <div class="card-shine"></div>
              <div class="case-study-icon">
                <div class="case-study-icon-bg"></div>
                <i class="fas ${project.icon}" aria-hidden="true"></i>
              </div>
              <div class="case-study-category">${project.category}</div>
              <h2 class="case-study-title" itemprop="name">${project.title}</h2>
              <p class="case-study-description" itemprop="description">${project.description}</p>
              <div class="tech-tags">
                ${project.technologies.map(tech => `
                  <span class="tech-tag">
                    <i class="${tech.icon}" aria-hidden="true"></i>
                    ${tech.name}
                  </span>
                `).join('')}
              </div>
              <a href="/professional-projects/?caseId=${project.id}" class="case-study-link" itemprop="url" aria-label="View case study for ${project.title}">
                View Case Study
                <i class="fas fa-arrow-right"></i>
              </a>
            </div>
          </div>
        </article>
      `).join('');
      
      // Add structured data for each project
      const projectStructuredData = caseStudiesArray.map((project, index) => ({
        "@context": "https://schema.org",
        "@type": "Project",
        "name": project.title,
        "description": project.description,
        "url": `https://dev-nkp.github.io/professional-projects/?caseId=${project.id}`,
        "keywords": project.keywords.join(', '),
        "author": {
          "@type": "Person",
          "name": "Niloy Kanti Paul"
        },
        "applicationCategory": "SoftwareApplication",
        "applicationSubCategory": "BusinessApplication",
        "operatingSystem": "Web",
        "dateCreated": "2023",
        "dateModified": "2023",
        "genre": project.category,
        "about": {
          "@type": "Thing",
          "name": project.title,
          "description": project.description
        }
      }));
      
      // Add structured data to the page
      const existingScript = document.querySelector('script[type="application/ld+json"]');
      if (existingScript) {
        existingScript.remove();
      }
      
      const script = document.createElement('script');
      script.type = 'application/ld+json';
      script.textContent = JSON.stringify({
        "@context": "https://schema.org",
        "@type": "CollectionPage",
        "name": "Professional Case Studies",
        "description": "Explore my portfolio of professional Salesforce development projects showcasing innovative solutions across multiple industries including travel, nonprofit, finance, healthcare, garments, and agriculture.",
        "url": "https://dev-nkp.github.io/professional-projects/",
        "mainEntity": projectStructuredData,
        "author": {
          "@type": "Person",
          "name": "Niloy Kanti Paul",
          "url": "https://dev-nkp.github.io"
        }
      });
      document.head.appendChild(script);
      
      // Add 3D tilt effect to cards
      setTimeout(() => {
        const cards = document.querySelectorAll('.card-tilt');
        
        cards.forEach(card => {
          card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            
            const rotateY = (x - centerX) / 50;
            const rotateX = (centerY - y) / 50;
            
            card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.05, 1.05, 1.05)`;
          });
          
          card.addEventListener('mouseleave', () => {
            card.style.transform = 'perspective(1000px) rotateX(0) rotateY(0) scale3d(1, 1, 1)';
          });
        });
      }, 100);
    }
    // Initialize theme
    function initializeTheme() {
      const savedTheme = localStorage.getItem('theme');
      
      if (savedTheme === 'dark') {
        document.body.classList.add('dark-body');
      } else {
        document.body.classList.remove('dark-body');
      }
    }
    // Filter functionality
    function initFilters() {
      const filterButtons = document.querySelectorAll('.filter-btn');
      
      filterButtons.forEach(button => {
        button.addEventListener('mouseenter', (e) => {
          const rect = button.getBoundingClientRect();
          const x = e.clientX - rect.left;
          const y = e.clientY - rect.top;
          
          button.style.setProperty('--mouse-x', `${x}px`);
          button.style.setProperty('--mouse-y', `${y}px`);
        });
        
        button.addEventListener('click', () => {
          // Remove active class from all buttons
          filterButtons.forEach(btn => btn.classList.remove('active'));
          
          // Add active class to clicked button
          button.classList.add('active');
          
          // Render case studies with selected filter
          renderCaseStudies(button.dataset.filter);
        });
      });
    }
    // Initialize page
    document.addEventListener('DOMContentLoaded', () => {
      initializeTheme();
      renderCaseStudies();
      initFilters();
    });
